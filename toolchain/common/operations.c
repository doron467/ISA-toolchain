#include "operations.h"

int getOperationsTable(Operation **operationsTable){
    static Operation operations[] = { /* addressing method 0 is most significant */
			{"mov",0xF,0x7,0}, {"cmp",0xF,0xF,1},
			{"add",0xF,0x7,2}, {"sub",0xF,0x7,3},
			{"lea",0x4,0x7,4}, {"clr",0x0,0x7,5}, {"not",0x0,0x7,6},
			{"inc",0x0,0x7,7}, {"dec",0x0,0x7,8},
			{"jmp",0x0,0xF,9}, {"bne",0x0,0xF,10},
			{"red",0x0,0x7,11}, {"prn",0x0,0xF,12},
			{"jsr",0x0,0xF,13}, {"rts",0x0,0x0,14}, {"stop",0x0,0x0,15},
			{"push",0x0,0xF,16},{"pop",0x0,0x7,17},
			{"be",0x0,0xF,18}, {"mod",0xF,0x7,19},
			{"mul",0xF,0x7,20}, {"div",0xF,0x7,21},
			{"null",0x0,0x0,-1}
	};

    *operationsTable = operations;
    return sizeof(operations) / sizeof(Operation) - 1;
}